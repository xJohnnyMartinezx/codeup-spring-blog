package com.codeup.codeupspringblog.web;

public class Post {


//    ****** Properties ********
    private String title;
    private String body;

//    ******* Getters and Setters ********


    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
